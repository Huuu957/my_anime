class AnimeModel {
  final String id;
  final String image;
  final String title;
  final String description;
  final int episodes;
  final double score;
  final String duration;

  AnimeModel({
    required this.id,
    required this.image,
    required this.title,
    required this.description,
    required this.episodes,
    required this.score,
    required this.duration,
  });
}
